/**
 * card/q1/q2 から LLM プロンプト用の文脈データを返す。
 * 範囲外の値や未定義は null/空文字を返す（fail-safe）。
 */
export function getPromptContext(card, q1, q2) {
  const c = SCENARIOS_SERVER[card];
  if (!c) return { q1_choice_text: '', q2_choice_text: '', q2_choice_type: '', sct_template: '' };
  const q1Text = (q1 >= 1 && q1 <= 4) ? (c.q1Choices[q1 - 1] || '') : '';
  const q2Branch = c.q2?.[q1];
  const q2Choice = (q2Branch && q2 >= 1 && q2 <= 4) ? (q2Branch.choices[q2 - 1] || null) : null;
  return {
    q1_choice_text: q1Text,
    q2_choice_text: q2Choice?.text || '',
    q2_choice_type: q2Choice?.type || '',
    sct_template: c.sctTemplate || '',
  };
}
